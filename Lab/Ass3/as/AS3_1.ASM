.MODEL SMALL
.STACK 100H
.DATA
	X DB 20 DUP(?)
.CODE
MAIN PROC
	MOV AX,@DATA 
	MOV DS,AX
	LEA SI,X
L1:
	MOV AH,01H
	INT 21H 
	CMP AL,13  ; CHECKING IF THE USER PRESSES ENTER 
	JE L2
	CMP AL,97 ; CHECKING IF IT IS IN LOWER CASE
	JGE L3
	JMP L4
L3:
	CMP AL,122
	JLE L5
	JMP L4
L5:
	SUB AL,32 ; CONVERTING LOWER TO UPPER CASE
L4:
	MOV [SI],AL
	INC SI
	JMP L1
L2:
	MOV CL,'$'
	MOV [SI],CL
	LEA DX,X   ;PRINTING THE ENTERED STRING IN UPPER CASE
	MOV AH,09H
	INT 21H
	MOV AH,4CH
	INT 21H
MAIN ENDP
END MAIN